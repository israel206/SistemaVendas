    public void preencherCombo(){
        //conexão com o banco postgres
        connFornecedor.conexao();
        //selecão da tabela banco todos os nnome de bairro
        connFornecedor.executaSQL("select * from bairros order by nome_bairro");
        jComboBoxBairro.removeAllItems();
        try {
            //setando para a primeira posição da chamada
            connFornecedor.rs.first();
            do{
                //preenchendo a comboBox com os dados da tabela Bairro, que veem do banco
                jComboBoxBairro.addItem(""+connFornecedor.rs.getString("nome_bairro"));
            } while(connFornecedor.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher os Bairros!\nERRO: "+ex);
        }
        // desconectando o banco
        connFornecedor.desconecta();
    }
    public void preencherTabela(String SQL){
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"ID", "Nomes", "CNPJ", "Cidades", "Estados"};
        
        connFornecedor.executaSQL(SQL);
        try {
            connFornecedor.rs.first();
            //preenchendo a tabela
            do {
                //buscar dados no resultset
                dados.add(new Object[]{connFornecedor.rs.getInt("id_fornecedor"),connFornecedor.rs.getString("nome_fornecedor"),connFornecedor.rs.getString("cnpj_fornecedor"),connFornecedor.rs.getString("nome_cidades"),connFornecedor.rs.getString("sigla_estado")});
                //qtd += +1;
            } while(connFornecedor.rs.next());
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList!\n ERRO: "+ex);
        }
        //JOptionPane.showMessageDialog(rootPane, "Quantidade de resgistros:" + qtd);
        //instancia do tabela
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableFornecedor.setModel(modelo);
        jTableFornecedor.getColumnModel().getColumn(0).setPreferredWidth(90);
        jTableFornecedor.getColumnModel().getColumn(0).setResizable(false);
        jTableFornecedor.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTableFornecedor.getColumnModel().getColumn(1).setResizable(false);
        jTableFornecedor.getColumnModel().getColumn(2).setPreferredWidth(210);
        jTableFornecedor.getColumnModel().getColumn(2).setResizable(false);
        jTableFornecedor.getColumnModel().getColumn(3).setPreferredWidth(210);
        jTableFornecedor.getColumnModel().getColumn(3).setResizable(false);
        jTableFornecedor.getColumnModel().getColumn(4).setPreferredWidth(60);
        jTableFornecedor.getColumnModel().getColumn(4).setResizable(false);
        jTableFornecedor.getTableHeader().setReorderingAllowed(false);
        jTableFornecedor.setAutoResizeMode(jTableFornecedor.AUTO_RESIZE_OFF);
        jTableFornecedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    
