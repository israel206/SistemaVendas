    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        //Utiizando logica de programação
        // ser for 1 salver os dados 
        if( mudar == 1){
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
            //faz a conversão para String e joga na comboBox
            mod.setIdBairro((String)jComboBoxBairro.getSelectedItem());
            control.Salvar(mod);
            //habilita os textFilds com = !true serve para inversão da chamada
            jTextFieldNome.setEnabled(!true);
            jTextFieldEndereco.setEnabled(!true);
            jFormattedTextFieldCNPJ.setEnabled(!true);
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            jTextFieldEndereco.setText("");
            jFormattedTextFieldCNPJ.setText("");
            //habilita os botões com = !true
            jButtonSalvar.setEnabled(!true);
            //desabilita os botões com = !false
            jButtonNovo.setEnabled(!false);
            preencherTabela("select * from fornecedores");
        // Caso aucontrario altere os dados
        } else{
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
            //faz a conversão para String e joga na comboBox
            mod.setIdBairro((String)jComboBoxBairro.getSelectedItem());
            control.Alterar(mod);
            //habilita os textFilds com = !true serve para inversão da chamada
            jTextFieldNome.setEnabled(!true);
            jTextFieldEndereco.setEnabled(!true);
            jFormattedTextFieldCNPJ.setEnabled(!true);
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            jTextFieldEndereco.setText("");
            jFormattedTextFieldCNPJ.setText("");
            //habilita os botões com = !true
            jButtonSalvar.setEnabled(!true);
            //desabilita os botões com = !false
            jButtonNovo.setEnabled(!false);
            preencherTabela("select * from fornecedores");
            //Codigo de verificação ser esta funcionando
            //JOptionPane.showMessageDialog(rootPane, "Alterar");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

