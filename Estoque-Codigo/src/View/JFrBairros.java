    public void preencherTabela(String SQL){
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"ID", "Bairros", "Cidades"};
        
        connBairro.executaSQL(SQL);
        try {
            connBairro.rs.first();
            //preenchendo a tabela
            do {
                //buscar dados no resultset
                dados.add(new Object[]{connBairro.rs.getInt("id_bairro"),connBairro.rs.getString("nome_bairro"),connBairro.rs.getString("nome_cidades")});
            } while(connBairro.rs.next());
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList!\n ERRO: " + ex);
        }
        //instancia do tabela
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableBairro.setModel(modelo);
        jTableBairro.getColumnModel().getColumn(0).setPreferredWidth(190);
        jTableBairro.getColumnModel().getColumn(0).setResizable(false);
        jTableBairro.getColumnModel().getColumn(1).setPreferredWidth(510);
        jTableBairro.getColumnModel().getColumn(1).setResizable(false);
        jTableBairro.getColumnModel().getColumn(2).setPreferredWidth(230);
        jTableBairro.getColumnModel().getColumn(2).setResizable(false);
        jTableBairro.getTableHeader().setReorderingAllowed(false);
        jTableBairro.setAutoResizeMode(jTableBairro.AUTO_RESIZE_OFF);
        jTableBairro.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    public void AtualizaCombo(){
        jComboBoxCidades.removeAllItems();
        try {
            connBairro.executaSQL("select * from cidades order by nome_cidades");
            connBairro.rs.first();
            do {
                jComboBoxCidades.addItem(connBairro.rs.getString("nome_cidades"));
            } while (connBairro.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher a combobox!\n ERRO: " + ex);
        }
    }
    
