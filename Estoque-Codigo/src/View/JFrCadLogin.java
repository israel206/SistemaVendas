    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        if(flag == 1){
            if(jPasswordFieldSenha_Login.getText().equals(jPasswordFieldConfirmacao_senha.getText())){
                mod.setLogin(jTextFieldLogin.getText());
                mod.setNomeCompleto(jTextFieldNome_Completo.getText());
                mod.setSenha(jPasswordFieldSenha_Login.getText());
                mod.setPermissao((String)jComboBoxPermissao.getSelectedItem());
                controlLogin.Salvar(mod);

                jTextFieldCod_Login.setText("");
                jTextFieldNome_Completo.setText("");
                jTextFieldLogin.setText("");
                jPasswordFieldSenha_Login.setText("");
                jPasswordFieldConfirmacao_senha.setText("");
                jButtonSalvar.setEnabled(!true);
                jTextFieldNome_Completo.setEnabled(!true);
                jTextFieldLogin.setEnabled(!true);
                jPasswordFieldSenha_Login.setEnabled(!true);
                jPasswordFieldConfirmacao_senha.setEnabled(!true);
                jButtonNovo.setEnabled(!false);
                jButtonCancelar.setEnabled(!true);
                jButtonEditar.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                preencherTabela();
            }else{
                JOptionPane.showMessageDialog(rootPane, "As senhas não corresponde!!!");
            }
        }else{
            if(jPasswordFieldSenha_Login.getText().equals(jPasswordFieldConfirmacao_senha.getText())){
                mod.setId(Integer.parseInt(jTextFieldCod_Login.getText()));
                mod.setLogin(jTextFieldLogin.getText());
                mod.setNomeCompleto(jTextFieldNome_Completo.getText());
                mod.setPermissao((String)jComboBoxPermissao.getSelectedItem());
                controlLogin.Alterar(mod);

                jTextFieldCod_Login.setText("");
                jTextFieldNome_Completo.setText("");
                jTextFieldLogin.setText("");
                jPasswordFieldSenha_Login.setText("");
                jPasswordFieldConfirmacao_senha.setText("");
                jButtonSalvar.setEnabled(!true);
                jTextFieldNome_Completo.setEnabled(!true);
                jTextFieldLogin.setEnabled(!true);
                jPasswordFieldSenha_Login.setEnabled(!true);
                jPasswordFieldConfirmacao_senha.setEnabled(!true);
                jButtonNovo.setEnabled(!false);
                jButtonCancelar.setEnabled(!true);
                jButtonEditar.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                flag = 1;
                preencherTabela();
            }else{
                JOptionPane.showMessageDialog(rootPane, "As senhas não corresponde!!!");
                
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

