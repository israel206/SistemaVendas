    public void preencherTabela(){
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"Cod_Usuario", "Nome Usuario", "Login", "Permissão"};
        connex.conexao();
        
        try{
            connex.executaSQL("select * from login");
            connex.rs.first();
            do {
                dados.add(new Object[]{connex.rs.getInt("id_login"),connex.rs.getString("nome_login"),connex.rs.getString("login"),connex.rs.getString("permissao")});
            } while(connex.rs.next());
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList!\n ERRO: " + ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableCadLogin.setModel(modelo);
        jTableCadLogin.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableCadLogin.getColumnModel().getColumn(0).setResizable(false);
        jTableCadLogin.getColumnModel().getColumn(1).setPreferredWidth(240);
        jTableCadLogin.getColumnModel().getColumn(1).setResizable(false);
        jTableCadLogin.getColumnModel().getColumn(2).setPreferredWidth(130);
        jTableCadLogin.getColumnModel().getColumn(2).setResizable(false);
        jTableCadLogin.getColumnModel().getColumn(3).setPreferredWidth(220);
        jTableCadLogin.getColumnModel().getColumn(3).setResizable(false);
        jTableCadLogin.getTableHeader().setReorderingAllowed(false);
        jTableCadLogin.setAutoResizeMode(jTableCadLogin.AUTO_RESIZE_OFF);
        jTableCadLogin.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
