    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (flag == 1){
            //mod.setId(Integer.parseInt(jTextFieldCodigo.getText()));
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setRg(jFormattedTextFieldRG.getText());
            mod.setCpf(jFormattedTextFieldCPF.getText());
            mod.setIdBairro((String) jComboBoxBairro.getSelectedItem());
            mod.setIdCidade((String) jLabelCidade.getText());
            mod.setIdTelefone((String) jComboBoxTelefone.getSelectedItem());
            control.Inserir(mod);
            jButtonSalvar.setEnabled(!true);
            jButtonCancelar.setEnabled(!true);
            jTextFieldEndereco.setEnabled(!true);
            jTextFieldNome.setEnabled(!true);
            jFormattedTextFieldCPF.setEnabled(!true);
            jFormattedTextFieldRG.setEnabled(!true);
            jButtonNovo.setEnabled(!false);
            jTextFieldEndereco.setText("");
            jTextFieldNome.setText("");
            preencherTabela();
        } else {
            mod.setId(Integer.parseInt(jTextFieldCodigo.getText()));
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setRg(jFormattedTextFieldRG.getText());
            mod.setCpf(jFormattedTextFieldCPF.getText());
            mod.setIdBairro((String) jComboBoxBairro.getSelectedItem());
            mod.setIdCidade((String) jLabelCidade.getText());
            mod.setIdTelefone((String) jComboBoxTelefone.getSelectedItem());
            control.Alterar(mod);
            jButtonSalvar.setEnabled(!true);
            jButtonCancelar.setEnabled(!true);
            jButtonEditar.setEnabled(!true);
            jTextFieldEndereco.setEnabled(!true);
            jTextFieldNome.setEnabled(!true);
            jFormattedTextFieldCPF.setEnabled(!true);
            jFormattedTextFieldRG.setEnabled(!true);
            jButtonNovo.setEnabled(!false);
            jTextFieldEndereco.setText("");
            jTextFieldNome.setText("");
            jTextFieldCodigo.setText("");
            flag = 1;
            preencherTabela();
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

