    public void preencherCombobox(){
        jComboBoxBairro.removeAllItems();
        jComboBoxTelefone.removeAllItems();
        
        connCliente.conexao();
        
        try {
            connCliente.executaSQL("select * from bairros order by nome_bairro");
            connCliente.rs.first();
            do {
                jComboBoxBairro.addItem(connCliente.rs.getString("nome_bairro"));
            } while (connCliente.rs.next());
            
            connCliente.executaSQL("select * from telefones order by numero_tel");
            connCliente.rs.first();
            do {
                jComboBoxTelefone.addItem(connCliente.rs.getString("numero_tel"));
            } while (connCliente.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher a comboBox!\n ERRO: " + ex);
        }
        connCliente.desconecta();
    }
    
     public void preencherTabela(){
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"Clientes", "Endereços", "telefones", "Cidades","Siglas"};
        connCliente.conexao();
        
        try{
            connCliente.executaSQL("select * from clientes inner join itens_tel_cli on clientes.id_cliente = itens_tel_cli.id_cliente inner join telefones on itens_tel_cli.id_tel = telefones.id_telefone inner join bairros on clientes.id_bairro = bairros.id_bairro inner join cidades on bairros.id_cidade = cidades.id_cidade inner join estados on cidades.id_cidade = estados.id_estado");
            connCliente.rs.first();
            do {
                dados.add(new Object[]{connCliente.rs.getString("nome_cliente"),connCliente.rs.getString("endereco_cliente"),connCliente.rs.getString("numero_tel"),connCliente.rs.getString("nome_cidades"),connCliente.rs.getString("sigla_estado")});
            } while(connCliente.rs.next());
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList!\n ERRO: " + ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableClientes.setModel(modelo);
        jTableClientes.getColumnModel().getColumn(0).setPreferredWidth(230);
        jTableClientes.getColumnModel().getColumn(0).setResizable(false);
        jTableClientes.getColumnModel().getColumn(1).setPreferredWidth(190);
        jTableClientes.getColumnModel().getColumn(1).setResizable(false);
        jTableClientes.getColumnModel().getColumn(2).setPreferredWidth(112);
        jTableClientes.getColumnModel().getColumn(2).setResizable(false);
        jTableClientes.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableClientes.getColumnModel().getColumn(3).setResizable(false);
        jTableClientes.getColumnModel().getColumn(4).setPreferredWidth(45);
        jTableClientes.getColumnModel().getColumn(4).setResizable(false);
        jTableClientes.getTableHeader().setReorderingAllowed(false);
        jTableClientes.setAutoResizeMode(jTableClientes.AUTO_RESIZE_OFF);
        jTableClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
